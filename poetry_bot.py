from sentence_transformers import SentenceTransformer, util
from rouge_score import rouge_scorer
import numpy as np
test_prompts = ["Write a poem about clouds"]

base_outputs = {
    "Write a poem about clouds":    '''clouds are like a soft and gentle rain,
each drop a poem in itself,
glistening in the sunlight,
like a thousand-colored dream.

a cloud overhead, like a delicate crown,
its edges softly brushed, like a whisper.
A gentle sway, as if it's trying to say
something beautiful, that's oh-so-rare.

a cloud that appears like a fairy tale,
its beauty shimmering, like magic.
A cloud that can make you feel lighter,
as if you could float off like a cloud.

a cloud that's as vast as the sky,'''
}

finetuned_outputs = {
    "Write a poem about clouds":    '''I’m in my room, looking out the window at the rain.
I could stop to see if it has stopped before or after falling
like gold coins into the gutters. But nothing is that simple,
so instead of stopping, I look back over my shoulder and walk slowly away from here, through the kitchen and onto the porch,
where I find my mother sitting on her front step with hands wrapped around two cups filled with tea,
waiting for me like this:
the way she always did. She was waiting for her son who never came when he said he would.
It’s not just rain now; I can smell it rising
in the air outside, cooling down'''
}

sem_model = SentenceTransformer("all-MiniLM-L6-v2")
scorer    = rouge_scorer.RougeScorer(['rouge1', 'rougeL'], use_stemmer=True)
results   = []

print("=" * 60)
print("TINYLLAMA: Base vs Fine-tuned Similarity")
print("=" * 60)

for prompt in test_prompts:
    base_text  = base_outputs[prompt]
    tuned_text = finetuned_outputs[prompt]

    emb_base  = sem_model.encode(base_text,  convert_to_tensor=True)
    emb_tuned = sem_model.encode(tuned_text, convert_to_tensor=True)
    cosine    = util.cos_sim(emb_base, emb_tuned).item()

    rouge  = scorer.score(base_text, tuned_text)
    rouge1 = rouge['rouge1'].fmeasure
    rougeL = rouge['rougeL'].fmeasure

    base_tok  = set(base_text.lower().split())
    tuned_tok = set(tuned_text.lower().split())
    jaccard   = len(base_tok & tuned_tok) / len(base_tok | tuned_tok) if (base_tok | tuned_tok) else 0

    results.append({"prompt": prompt, "cosine": cosine,
                    "rouge1": rouge1, "rougeL": rougeL, "jaccard": jaccard})

    print(f"\nPrompt : {prompt}")
    print(f"  Cosine  : {cosine:.4f}")
    print(f"  ROUGE-1 : {rouge1:.4f}")
    print(f"  ROUGE-L : {rougeL:.4f}")
    print(f"  Jaccard : {jaccard:.4f}")

print("\n" + "=" * 60)
print("AVERAGES")
print("=" * 60)
print(f"Cosine  : {np.mean([r['cosine']  for r in results]):.4f}")
print(f"ROUGE-1 : {np.mean([r['rouge1']  for r in results]):.4f}")
print(f"ROUGE-L : {np.mean([r['rougeL']  for r in results]):.4f}")
print(f"Jaccard : {np.mean([r['jaccard'] for r in results]):.4f}")