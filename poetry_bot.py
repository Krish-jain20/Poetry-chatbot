import json
import re

def clean_text(text):
    # Remove \r characters
    text = text.replace('\r', '')
    # Collapse multiple newlines into one
    text = re.sub(r'\n{3,}', '\n\n', text)
    # Remove excessive spaces/tabs
    text = re.sub(r'[ \t]{2,}', ' ', text)
    # Clean up messy title instructions like "Write a poem titled '\n    Title\n'"
    text = re.sub(r"titled '\s+(.+?)\s+'", lambda m: f"titled '{m.group(1).strip()}'", text)
    return text.strip()

