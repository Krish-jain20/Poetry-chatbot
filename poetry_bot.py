def convert_to_instruction_format(example):
    poem = example['Poem']
    tags = example['Tags']
    title = example['Title']

    # Handle rows with null/missing tags
    if tags and tags.strip():
        # Tags exist → use first tag as topic
        first_tag = tags.split(',')[0].strip()
        instruction = f"Write a poem about {first_tag}"
    elif title and title.strip():
        # No tags → use title as hint
        instruction = f"Write a poem titled '{title}'"
    else:
        # Fallback
        instruction = "Write a poem"

    # Format in LLaMA 2 [INST] style
    formatted = f"<s>[INST] {instruction} [/INST] {poem.strip()} </s>"

    return {"text": formatted}

